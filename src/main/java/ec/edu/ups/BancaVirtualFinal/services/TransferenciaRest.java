package ec.edu.ups.BancaVirtualFinal.services;

public class TransferenciaRest {
	private String cedula;
	private String cuentaDeAhorro;
	private double monto;
	/**
	 * @return the cedula
	 */
	public String getCedula() {
		return cedula;
	}
	/**
	 * @param cedula the cedula to set
	 */
	public void setCedula(String cedula) {
		this.cedula = cedula;
	}
	/**
	 * @return the cuentaDeAhorro
	 */
	public String getCuentaDeAhorro() {
		return cuentaDeAhorro;
	}
	/**
	 * @param cuentaDeAhorro the cuentaDeAhorro to set
	 */
	public void setCuentaDeAhorro(String cuentaDeAhorro) {
		this.cuentaDeAhorro = cuentaDeAhorro;
	}
	/**
	 * @return the monto
	 */
	public double getMonto() {
		return monto;
	}
	/**
	 * @param monto the monto to set
	 */
	public void setMonto(double monto) {
		this.monto = monto;
	}
	
	

}
